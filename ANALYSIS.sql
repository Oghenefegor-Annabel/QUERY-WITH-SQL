select 
	CITY,
	COUNT(QUANTITYORDERED) as City_Order_Count
from sales_data_sample
where CITY IN ('NYC', 'Lille', 'Paris','San Francisco', 'Chatswood')
GROUP BY CITY
having COUNT(QUANTITYORDERED) > 10; 


